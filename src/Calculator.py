class Calculator:

    def plus(self, x, y):
        return int(x) + int(y)

    def minus(self, x, y):
        return int(x) - int(y)

    def multiply(self, x, y):
        return int(x) * int(y)

    def divide(self, x, y):
        return float(x) / float(y)

    def squared(self, x):
        return int(x) ** 2

    def sqrt(self, x):
        return float(x) ** (1 / 2)




